# -*- coding: utf-8 -*-
"""母差保护 主Agent Prompt"""


def build_main_agent_prompt(paragraphs_content: str) -> str:
    return f"""# 主 Agent 母差跳闸简报生成 Prompt

## 任务

**只读段落文件**，基于所有 Subagent 生成的装置段落，生成完整的母差跳闸简报。

**严格禁止：读取原始 hdr/rms/events/csv/dat 文件**

## 输入段落

{paragraphs_content}

## 母差保护简报结构

请按以下结构生成简报：

```markdown
# {{日期}} {{厂站名称}}母差跳闸简报

## 一、故障基本情况

| 项目 | 内容 |
|------|------|
| 故障时间 | {{从段落提取}} |
| 厂站 | {{从段落提取}} |
| 故障设备 | {{母线名称}} |
| 故障段 | {{I母/II母}} |
| 故障相别 | {{综合判断}} |
| 故障类型 | 瞬时性/永久故障 |
| 保护动作情况 | {{各套动作汇总}} |
| 故障电流 | {{取自段落 HDR FaultInfo 故障相电流（一次值），不使用 RMS 最大值或突变值}} |

## 二、保护配置情况

### 2.1 保护装置信息表
| 厂站 | 套别 | 装置型号 | 制造厂家 | 软件版本 | 备注 |
|------|------|----------|----------|----------|------|

## 三、动作基本情况
{{按套别叙述}}

## 四、动作时序表

### 4.1 时间对齐基准

**第一步：电流正突变对齐**

| 套别 | 正突变最大值(A) | 正突变绝对时间 |
|------|----------------|----------------|

**第二步：保护启动对齐**

### 4.2 动作时序表（仅动作事件）

*基准时间：{{最早的保护启动绝对时间}}*

| 绝对时间 | 第1套 | 第2套 |
|----------|-------|-------|
| HH:MM:SS.mmm | 事件 | - |

## 五、通道对应关系分析
{{间隔映射验证 + 同名相一致性校验}}

## 六、差流分析
{{大差/小差 + 各间隔电流矢量和}}

## 七、保护动作评价

## 八、故障录波评价（如有）
```

## 可信度原则

**只写有依据的数据，不确定的数据必须标注。**

### 交叉验证
| 校验项 | 规则 | 异常标注 |
|--------|------|----------|
| 大差/小差 | 大差 = 各间隔电流矢量和 | `[待核实: 差流计算异常]` |
| 动作时间 | 同厂站两套动作时间差 < 20ms | `[待核实: 动作时间差较大]` |
| 通道对应 | 同一间隔两套保护电流应一致 | `[待核实: 通道映射不一致]` |

## 时序信息融合规则

**核心原则：动作时序表必须使用绝对时间（HH:MM:SS.mmm），所有事件必须转换为统一的绝对时间后填入时序表。严禁使用相对时间（ms偏移量）作为时序表的时间列。**

### 绝对时间计算方法

| 时间来源 | 转换规则 |
|----------|----------|
| Events CSV | 已是绝对时间（HH:MM:SS.mmm），直接使用 |
| HDR TripInfo 偏移量 | **绝对时间 = 该套电流正突变绝对时间 + HDR偏移量**（首选）；若无正突变数据，用 Events 中保护启动绝对时间作为基准 |
| 段落中的时间 | 已是绝对时间格式（HH:MM:SS.mmm），直接使用 |

### 常见错误（严禁）
- ❌ 将某套 HDR 的偏移量（如 `20ms`）直接当作全局相对时间填入时序表
- ❌ 用某个统一基准时间减去各套事件时间来生成"全局相对时间"列

### 时序表填写规则
1. 时间列必须为绝对时间格式 `HH:MM:SS.mmm`
2. 每个事件的绝对时间必须独立计算，不同装置有不同的时间基准
3. 动作时序表中不包含电流正负突变行
4. **故障电流必须使用段落 HDR FaultInfo 中的故障相电流（一次值），不得使用 RMS 最大值或电流突变值**
4. HDR 中有但 Events 中无的信号，在时序表中标注 `_hdr` 后缀
"""
