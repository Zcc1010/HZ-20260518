# -*- coding: utf-8 -*-
"""母差保护 Subagent Prompt"""


def build_subagent_prompt(
    hdr_content: str,
    rms_content: str,
    events_content: str,
    station: str,
    set_number: str,
) -> str:
    return f"""# 母差保护 Subagent Prompt 模板

## 任务

给定本套母差保护装置的录波文件（hdr + rms.csv + events.csv），按 HDR/Events/RMS 三个区块提取本套装置的关键信息，生成标准化的 Markdown 段落。

## 核心原则（必须遵守）

1. **严禁编造数据**：所有数据必须来自输入文件，不得凭空推测或编造。无数据时标注 `[无数据]`。
2. **数据溯源**：关键数据必须标注来源文件和字段名。
3. **保留原始值**：提取的数据必须与源文件一致，不得修改或"修正"原始数值。

## 设备信息

- 厂站: {station}
- 套别: {set_number}

## 输入数据

### HDR 文件内容
{hdr_content if hdr_content else '[HDR文件缺失]'}

### RMS CSV 文件内容
{rms_content if rms_content else '[RMS文件缺失]'}

### Events CSV 文件内容
{events_content if events_content else '[Events文件缺失]'}

## 输出格式

**重要：直接输出 Markdown 内容，不要用 ```markdown``` 代码围栏包裹。**

严格按照以下 Markdown 格式输出。有数据的字段填写实际值，无数据的标注`[无数据]`。

**精简原则**：
- 当整个区块无数据时（如 HDR 文件缺失），只写一行 `[HDR文件缺失]`，**不要**逐字段列出 [无数据]
- Events 表只列出"动作"事件，省略"返回"事件
- RMS 信息只列出有突变值的通道
- **数字状态（DigitalStatus）只列出值为1的条目**，值为0的条目全部省略（包括"备用: 0"、未动作的保护功能等），不要逐条列出

## {station}_{set_number}

**装置型号**: {{型号}}，{{程序版本}}，{{智能站/常规站}}
**制造厂家**: {{厂家名称}}
**故障时间**: {{年}}-{{月}}-{{日}} {{时}}:{{分}}:{{秒}}.{{毫秒}}

### HDR信息
**设备信息**:
- 一次设备名称: {{值}}
- 装置型号: {{值}}
- 程序版本: {{值}}

**TripInfo（保护动作时间序列）**:
| 绝对时间 | 动作名称 | 相别 | 值 |
|----------|----------|------|-----|

**DigitalEvent（数字量事件）**:
| 绝对时间 | 名称 | 变化 |
|----------|------|------|

**FaultInfo（故障信息）**:
- 故障相电流: {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}）或 {{值}}A [无CT变比]
- 最大差动电流: {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}）或 {{值}}A [无CT变比]
- 故障相电压: {{一次值}}kV（二次值{{值}}V，PT变比{{值}}kV/{{值}}V）或 {{值}}V [无PT变比]
- 最大零序电流: {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}）或 {{值}}A [无CT变比]

**定值信息**:
- 母差保护定值: {{值}}A
- 复合电压闭锁定值: {{值}}V
- 断路器失灵电流定值: {{值}}A
- 停用重合闸: {{1=停用/0=未停用}}
- CT变比: {{CT一次额定值}}/{{CT二次额定值}} 或 [无数据]
- PT变比: {{PT一次额定值}}kV 或 [无数据]

### Events信息
| 绝对时间 | 通道名称 | 内容 |
|----------|----------|------|

### RMS信息

**重要：rms.csv 中的"RMS最大值"和"RMS最小值"都是二次侧测量值（单位为A或V），不是一次值！**
- **RMS最大值** = 该通道在录波期间的二次侧RMS最大值
- **RMS最小值** = 该通道在录波期间的二次侧RMS最小值，不是"二次值"的含义
- **一次值换算**：有CT/PT变比时，一次值 = 二次值 × CT一次额定值 / CT二次额定值 / 1000（电流kA）；电压类似
- **无CT/PT变比时**：只显示二次值，标注 [无CT变比] 或 [无PT变比]

**大差电流RMS最大值（二次值）**:
- I母大差: {{RMS最大值}}A 或 {{一次值}}kA（二次值{{RMS最大值}}A，CT变比...）
- II母大差: {{RMS最大值}}A 或 {{一次值}}kA（二次值{{RMS最大值}}A，CT变比...）

**各间隔电流RMS最大值（二次值）**:
- {{间隔1}}: IA: {{RMS最大值}}A, IB: ..., IC: ...
  （有CT变比时补充一次值）
- {{间隔2}}: IA: ..., IB: ..., IC: ...

**电压RMS最小值（二次值）**:
- UA: {{RMS最小值}}V, UB: ..., UC: ...
  （有PT变比时补充一次值）

**电流电压突变**（突变值为二次侧值，单位A或V）:
**第一次突变**（故障发生时）:
- {{间隔名}}{{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- {{间隔名}}{{通道名}}负突变: -{{值}}A @ HH:MM:SS.mmm
- ...（列出所有突变幅度超过阈值的通道，涵盖各间隔通道）
- 如果某个通道没有检测到突变（突变值为空），说明该通道在故障期间变化平稳，不需要列出

**第二次突变**（重合后，如有）:
- {{间隔名}}{{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- ...（无第二次突变时不输出此分组）

**通道对应关系**:
- {{间隔1}} Ia/Ib/Ic → {{I段/II段}}
- {{间隔2}} Ia/Ib/Ic → {{I段/II段}}

> 数据来源: {{hdr文件名}} + {{rms文件名}} + {{events文件名}}

## 关键提取规则

1. 厂站名和套别：{station}_{set_number}

2. 装置型号：从 HDR `<DeviceInfo>` 提取，根据型号后缀判断智能站/常规站（含 FA/DA/DG → 智能站，其他 → 常规站）

3. 故障时间：从 HDR `<FaultStartTime>` 提取

4. TripInfo：从 HDR `<TripInfo>` 提取，偏移量转绝对时间

   | HDR字段 | 说明 |
   |---------|------|
   | time | 偏移量（ms），需转换为绝对时间 |
   | name | 动作名称 |
   | phase | 相别 |
   | value | 1=动作，0=返回 |

   **严禁输出相对时间**（如 `0ms`、`15ms`），所有时间必须为绝对时间 `HH:MM:SS.mmm`

5. DigitalEvent：从 HDR `<DigitalEvent>` 提取

   | HDR字段 | 说明 |
   |---------|------|
   | time | 偏移量（ms），需转换为绝对时间 |
   | name | 事件名称 |
   | value | 变化描述（如 0→1、1→0） |

6. 定值信息：从 HDR `<SettingValue>` 提取关键定值：

   | 定值名称 | 说明 | 单位 |
   |----------|------|------|
   | 母差保护定值 | 母差保护动作电流定值 | A |
   | 复合电压闭锁定值 | 复合电压闭锁电压定值 | V |
   | 断路器失灵电流定值 | 失灵保护电流定值 | A |
   | 停用重合闸 | 停用重合闸投退（1=停用） | - |
   | CT一次额定值 | CT 一次侧额定电流 | A |
   | CT二次额定值 | CT 二次侧额定电流（通常1A或5A） | A |
   | PT一次额定值 | PT 一次侧额定电压 | kV |

7. Events信息：从 events.csv 提取

8. RMS信息：从 rms.csv 提取大差电流、各间隔电流、电压、突变（按第一次/第二次分组）。**rms.csv 中的值都是二次侧值**，有CT/PT变比时换算为一次值，无变比时只显示二次值

9. 通道对应关系：从 cfg 文件或 HDR 通道定义中提取间隔映射（每个间隔的 Ia/Ib/Ic 对应 I段 还是 II段）

10. CT变比和PT变比：从 HDR `<SettingValue>` 提取（**不从 CFG 提取**）：

    | 字段名 | 说明 | 单位 |
    |--------|------|------|
    | CT一次额定值 | CT 一次侧额定电流 | A |
    | CT二次额定值 | CT 二次侧额定电流（通常 1A 或 5A） | A |
    | PT一次额定值 | PT 一次侧额定电压 | kV |

    **CT变比** = CT一次额定值 / CT二次额定值（如 3000/1）

11. FaultInfo：从 HDR `<FaultInfo>` 提取故障时刻的关键电气量，全部换算为一次值：

    | 字段名 | 说明 | 显示格式 |
    |--------|------|----------|
    | 故障相电流 | 故障相电流二次值 | {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}） |
    | 最大差动电流 | 差动电流最大值 | 同上 |
    | 故障相电压 | 故障相电压二次值 | {{一次值}}kV（二次值{{值}}V，PT变比{{值}}kV/{{值}}V） |
    | 最大零序电流 | 零序电流最大值 | 同电流格式 |

12. 一次值换算：电流一次值(kA) = 二次值(A) × CT一次额定值 / CT二次额定值 / 1000；无变比时标注 [无CT变比]

## 错误处理
- 文件缺失：标注`[本套文件缺失]`
- 字段无数据：标注`[无数据]`
"""
