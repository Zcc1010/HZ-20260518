# -*- coding: utf-8 -*-
"""故障录波 Subagent Prompt"""


def build_subagent_prompt(
    rpt_content: str,
    ana_content: str = "",
    inf_content: str = "",
    hdr_content: str = "",
    station: str = "",
    set_number: str = "",
) -> str:
    label = f"故障录波_{station}" if not set_number else f"故障录波_{station}_{set_number}"
    return f"""# 故障录波 Subagent Prompt 模板

## 任务

给定故障录波器的解析数据（RPT 故障分析报告 + ANA 装置事件 + INF 通道配置 + HDR 装置参数），提取关键信息，生成标准化的 Markdown 段落。

## 核心原则（必须遵守）

1. **严禁编造数据**：所有数据必须来自输入文件，不得凭空推测或编造。无数据时标注 `[无数据]`。
2. **数据溯源**：关键数据必须标注来源文件和字段名。
3. **保留原始值**：提取的数据必须与源文件一致，不得修改或"修正"原始数值。
4. **只提取故障设备相关内容**，忽略其他线路/设备的数据。
5. **数字状态（DigitalStatus）只列出值为1的条目**，值为0的条目全部省略。

## 设备信息

- 厂站: {station}

## 输入数据

### RPT 文件内容（故障分析报告）
{rpt_content if rpt_content else '[RPT文件缺失]'}

### ANA 文件内容（装置事件数据）
{ana_content if ana_content else '[ANA文件缺失]'}

### INF 文件内容（通道配置）
{inf_content if inf_content else '[INF文件缺失]'}

### HDR 文件内容（装置参数）
{hdr_content if hdr_content else '[HDR文件缺失]'}

## 输出格式

**重要：直接输出 Markdown 内容，不要用 ```markdown``` 代码围栏包裹。**

严格按照以下 Markdown 格式输出。有数据的字段填写实际值，无数据的标注`[无数据]`。

**精简原则**：
- 当整个区块无数据时（如 RPT 文件缺失），只写一行 `[RPT文件缺失]`，**不要**逐字段列出 [无数据]
- 开关量变位只列出与故障设备相关的条目
- **数字状态（DigitalStatus）只列出值为1的条目**，值为0的条目全部省略
- 同一设备同一事件（如断路器B相位置/总位置）只保留首次出现的一对（分位+合位），不要重复列出

## {label}

**录波器型号**: {{型号}}
**故障时间**: {{年}}-{{月}}-{{日}} {{时}}:{{分}}:{{秒}}.{{毫秒}}

### 故障分析报告（RPT）

**基本信息**:
- 变电站: {{值}}
- 录波触发时间: {{值}}
- 故障次数: {{N}}次

**各次故障摘要**:
| 序号 | 故障元件 | 故障类型 | 跳闸相别 | 故障距离 | 保护动作时间 |
|------|----------|----------|----------|----------|-------------|
（从 RPT 故障分析报告提取，每次故障一行）

**第1次故障详情**:
- 故障元件: {{线路名}}
- 故障类型: {{AN/CA/...}}
- 故障相别: {{A/B/C/...}}
- 跳闸相别: {{A/B/C/ABC}}
- 故障起始时间: {{ms}}
- 故障结束时间: {{ms}}
- 保护动作时间: {{ms}}
- 故障距离: {{km}}
- 二次侧电抗: {{Ω}}
- 故障过程中最小电压有效值: {{kV}}
- 故障过程中最大电流有效值: {{kA}}

**故障电气量相量**（第一次故障）:

**故障前一周波电压**:
| 相别 | 一次值 | 二次值 | 相角 |
|------|--------|--------|------|
| A相 | {{kV}} | {{V}} | {{°}} |
| B相 | {{kV}} | {{V}} | {{°}} |
| C相 | {{kV}} | {{V}} | {{°}} |
| N相 | {{kV}} | {{V}} | {{°}} |
| 正序 | {{kV}} | {{V}} | {{°}} |
| 负序 | {{kV}} | {{V}} | {{°}} |

**故障时电压**:
（同上格式）

**故障时电流**:
| 相别 | 一次值 | 二次值 | 相角 |
|------|--------|--------|------|
| A相 | {{kA}} | {{A}} | {{°}} |
| B相 | {{kA}} | {{A}} | {{°}} |
| C相 | {{kA}} | {{A}} | {{°}} |
| N相 | {{kA}} | {{A}} | {{°}} |
| 正序 | {{kA}} | {{A}} | {{°}} |
| 负序 | {{kA}} | {{A}} | {{°}} |

**故障后一周波电压/电流**:
（同上格式）

**故障后二周波电压/电流**:
（同上格式）

**线路参数**:
- 名义参数: R1={{Ω/km}}, X1={{Ω/km}}, R0={{Ω/km}}, X0={{Ω/km}}
- 实测参数: R1={{Ω/km}}, X1={{Ω/km}}, R0={{Ω/km}}, X0={{Ω/km}}
- 保护边界参数: ZR={{Ω}}, ZX={{Ω}}, ARG={{°}}

（如有第2次及以后的故障，格式同上）

### 开关量变位清单（RPT）

| 通道名称 | 信号类型 | 变位/复位 | 相对时间(ms) | 采样值 |
|----------|----------|-----------|-------------|--------|
（从 RPT 开关量变位清单提取，只列出与故障设备相关的通道）

### 保护动作时序（ANA TripInfo）

| 动作名称 | 动作时间(ms) | 绝对时间 | 相别 | 值 |
|----------|-------------|----------|------|-----|
（从 ANA TripInfo 提取，只列出 value=1 的动作事件）

### 装置信息（ANA）

- 厂站名称: {{值}}
- 录波器名称: {{值}}
- 装置型号: {{值}}
- 程序版本: {{值}}

### 故障信息（ANA）

- 故障设备名称: {{值}}
- 故障相别: {{值}}
- 区内/区外故障: {{值}}
- 重合闸情况: {{值}}

### 通道配置（INF）

**录波器**: {{录波器名称}}

| 通道ID | 相别 | 监控组件 | 单位 | CT/PT变比 |
|--------|------|----------|------|-----------|
（从 INF 通道配置提取关键通道，如电压、电流通道）

> 数据来源: {{rpt文件名}} + {{ana文件名}} + {{inf文件名}} + {{hdr文件名}}

## 关键提取规则

1. 厂站名：{label}
2. 录波器型号：从 ANA `<DeviceInfo>` 提取
3. 故障时间：从 RPT "录波触发时间" 或 ANA `<FaultStartTime>` 提取
4. 故障分析报告：从 RPT 解析，包含每次故障的详细电气量数据
5. 开关量变位：从 RPT "开关量变位清单" 提取
6. TripInfo：从 ANA `<TripInfo>` 提取，偏移量已转换为绝对时间
7. DigitalEvent：从 ANA `<DigitalEvent>` 提取
8. DigitalStatus：从 ANA `<DigitalStatus>` 提取，**只保留 value=1 的条目**
9. 通道配置：从 INF 提取 CT/PT 变比和通道映射
10. 数值型字段必须忠实于原始数据，即使值为0也要如实填写，不得省略或替换

## 错误处理
- 文件缺失：标注`[录波文件缺失]`
- 字段无数据：标注`[无数据]`
"""
