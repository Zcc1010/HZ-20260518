# -*- coding: utf-8 -*-
"""线路保护 Subagent Prompt"""


def build_subagent_prompt(
    hdr_content: str,
    rms_content: str,
    events_content: str,
    station: str,
    set_number: str,
) -> str:
    return f"""# 线路保护 Subagent Prompt 模板

## 任务

给定本套保护装置的录波文件（hdr + rms.csv + events.csv），按 HDR/Events/RMS 三个区块提取本套装置的关键信息，生成标准化的 Markdown 段落。

## 核心原则（必须遵守）

1. **严禁编造数据**：所有数据必须来自输入文件，不得凭空推测或编造设备编号、变比、定值等信息。无数据时标注 `[无数据]`。
2. **数据溯源**：关键数据必须标注来源文件和字段名。
3. **保留原始值**：提取的数据必须与源文件一致，不得修改或"修正"原始数值。

## 设备信息

- 厂站: {station}
- 套别: {set_number}

## 输入数据

### HDR 文件内容
{hdr_content if hdr_content else '[HDR文件缺失]'}

### RMS CSV 文件内容
{rms_content if rms_content else '[RMS文件缺失]'}

### Events CSV 文件内容
{events_content if events_content else '[Events文件缺失]'}

## 输出格式

**重要：直接输出 Markdown 内容，不要用 ```markdown``` 代码围栏包裹。**

严格按照以下 Markdown 格式输出。有数据的字段填写实际值，无数据的标注`[无数据]`，有推测规则的标注`[推测为XXX]`。

**精简原则**：
- 当整个区块无数据时（如 HDR 文件缺失），只写一行 `[HDR文件缺失]`，**不要**逐字段列出 [无数据]
- Events 表只列出"动作"事件，省略"返回"事件（减少冗余）
- RMS 信息只列出有突变值的通道
- **数字状态（DigitalStatus）只列出值为1的条目**，值为0的条目全部省略（包括"备用: 0"、未动作的保护功能等），不要逐条列出
- 录波器段落中的开关动作信息：同一设备同一事件（如断路器B相位置/总位置）只保留首次出现的一对（分位+合位），不要重复列出

输出格式模板（注意：模板中的 `{{xxx}}` 表示需要填入实际值的位置，无数据时表格内填 `-`，非表格位置写明缺失原因，**严禁输出 `{{xxx}}` 占位符**）：

## {station}_{set_number}

**装置型号**: {{型号}}，{{程序版本}}，{{智能站/常规站}}
**制造厂家**: {{厂家名称}}
**故障时间**: {{年}}-{{月}}-{{日}} {{时}}:{{分}}:{{秒}}.{{毫秒}}

### HDR信息
（如果 HDR 有内容：正常列出设备信息、TripInfo、DigitalEvent、FaultInfo、定值信息）
（如果 HDR 缺失或内容为空：只写 `[HDR文件缺失]`，不要展开子字段）

### Events信息
| 绝对时间 | 通道名称 | 内容 |
|----------|----------|------|
| HH:MM:SS.mmm | {{通道名}} | 动作 |

### RMS信息
**电流RMS最大值**: IA: {{一次值}}kA（二次值{{值}}A）, IB: {{一次值}}kA（二次值{{值}}A）, IC: {{一次值}}kA（二次值{{值}}A）, 3I0: {{一次值}}kA（二次值{{值}}A）
（无CT变比时只显示二次值: IA: {{值}}A [无CT变比]）
**电压RMS最小值**: UA: {{一次值}}kV（二次值{{值}}V）, UB: {{一次值}}kV（二次值{{值}}V）, UC: {{一次值}}kV（二次值{{值}}V）
（无PT变比时只显示二次值: UA: {{值}}V [无PT变比]）

### 差流分析

**差流数据（直接从文件提取，无需计算）**

| 通道 | A相差流 | B相差流 | C相差流 |
|------|---------|---------|---------|
| 通道一 | {{值}} A | {{值}} A | {{值}} A |
| 通道二 | {{值}} A | {{值}} A | {{值}} A |

- **故障相电流**: {{值}} A（来自 FaultInfo）
- **最大差动电流**: {{值}} A（来自 FaultInfo）
- **3I0**: {{值}} A（来自 FaultInfo）

（数据来自 HDR 的 TripInfo/FaultInfo，获取不到时写"未获取到数据"）

**结果汇总**

- **故障相**: {{相别}}（差流最大相）
- **最大差流**: {{一次值}} kA（通道一/通道二{{是否一致}}）
- **故障电流**: {{一次值}} kA
- **差流与故障电流比值**: {{百分比}}%
- **通道一致性**: 通道一与通道二差流{{是否一致}}，偏差{{百分比}}%（{{正常/异常}}）
（注意：故障相电流与差流是不同概念，不得混用）

**电流电压突变**:
**第一次突变**（故障发生时）:
- {{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- {{通道名}}负突变: -{{值}}A @ HH:MM:SS.mmm
- ...（列出所有突变幅度超过阈值的通道）

**第二次突变**（重合后，如有）:
- {{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- ...（无第二次突变时不输出此分组）

> 数据来源: {{hdr文件名}} + {{rms文件名}} + {{events文件名}}

## 字段提取规则

### 数据来源优先级（来自 comtrade-web 最新规则）
- **优先从 HDR FaultInfo/故障信息中提取**
- 如果 HDR 无该信息，则从模拟通道 RMS 统计和突变数据中取值（标注"[推断]"）

### 故障类型判断
- 如果重合闸动作后线路持续运行（无后续分闸），则为"瞬时故障"
- 否则为"永久故障"（包括重合闸未动作、重合不成功等）

### 故障相别判断
- 从 HDR FaultInfo 中取
- 无则根据电压最小有效值通道和电流最大有效值通道推断

### 故障测距
- HDR 有则取（注意：0.00也是有效数据，必须如实填写"0.00km"）
- 无则标注"[无数据]"

### 时间格式规范
- **日期**：只写一次（在段落开头故障时间字段中）
- **绝对时间**：格式 `HH:MM:SS.mmm`
- **严禁输出相对时间**（如 `0ms`、`15ms`），所有时间必须为绝对时间 `HH:MM:SS.mmm`
- HDR 中时间是偏移量（如 `0ms`、`11ms`），需转换为绝对时间：绝对时间 = 故障起始时间 + ms偏移量

### 关键提取规则
1. 厂站名和套别：从目录结构或文件名中提取
2. 装置型号：从 HDR `<DeviceInfo>` 提取，根据型号后缀判断智能站/常规站（含 FA/DA/DG → 智能站，其他 → 常规站）
3. 故障时间：从 HDR `<FaultStartTime>` 提取
4. TripInfo：从 HDR `<TripInfo>` 提取，偏移量转绝对时间

   | HDR字段 | 说明 |
   |---------|------|
   | time | 偏移量（ms），需转换为绝对时间 |
   | name | 动作名称 |
   | phase | 相别（如ABC、A相、无） |
   | value | 1=动作，0=返回 |

   **转换方法**：绝对时间 = 故障起始时间 + time偏移
   例：故障起始 16:33:20.208，HDR中 time=107ms → 绝对时间 = 16:33:20.315

5. DigitalEvent：从 HDR `<DigitalEvent>` 提取

   | HDR字段 | 说明 |
   |---------|------|
   | time | 偏移量（ms），需转换为绝对时间 |
   | name | 事件名称 |
   | value | 变化描述（如 0→1、1→0） |

6. 定值信息：从 HDR `<SettingValue>` 提取关键定值：

   | 定值名称 | 说明 | 单位 |
   |----------|------|------|
   | 差动动作电流定值 | 差动保护动作电流定值 | A |
   | 线路总长度 | 线路全长 | km |
   | 接地距离Ⅰ段定值 | 接地距离I段阻抗定值 | Ω |
   | 接地距离Ⅱ段定值 | 接地距离II段阻抗定值 | Ω |
   | 接地距离Ⅱ段时间 | 接地距离II段时间 | s |
   | 相间距离Ⅰ段定值 | 相间距离I段阻抗定值 | Ω |
   | 零序过流Ⅲ段定值 | 零序过流III段电流定值 | A |
   | 停用重合闸 | 停用重合闸投退（1=停用） | - |
   | 单相重合闸时间 | 单相重合闸时间定值 | s |
   | 三相重合闸时间 | 三相重合闸时间定值 | s |
   | CT一次额定值 | CT一次侧额定电流 | A |
   | CT二次额定值 | CT二次侧额定电流（通常1A或5A） | A |
   | PT一次额定值 | PT一次侧额定电压 | kV |

   - 只提取与保护动作相关的定值和时限，不要提取非保护动作定值（如通道参数、装置参数等）

7. Events信息：从 events.csv 提取，保持原始通道名，只陈述事实
8. RMS信息：从 rms.csv 提取最大值/最小值/突变（按第一次/第二次分组，列出所有超过阈值的通道）
9. CT变比和PT变比：从 HDR `<SettingValue>` 提取（**不从 CFG 提取**，CFG 通常为 1:1 不准确）：

   | 字段名 | 说明 | 单位 |
   |--------|------|------|
   | CT一次额定值 | CT 一次侧额定电流 | A |
   | CT二次额定值 | CT 二次侧额定电流（通常 1A 或 5A） | A |
   | PT一次额定值 | PT 一次侧额定电压 | kV |

   **CT变比** = CT一次额定值 / CT二次额定值（如 3000/1）
   HDR 中无 CT/PT 变比字段时，标注 `[无数据]`。

10. FaultInfo：从 HDR `<FaultInfo>` 提取以下字段，全部换算为一次值：

    | 字段名 | 说明 | 显示格式 |
    |--------|------|----------|
    | 故障相电流 | 故障相电流二次值 | {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}） |
    | 最大差动电流 | 差动电流最大值 | 同上 |
    | 故障相电压 | 故障相电压二次值 | {{一次值}}kV（二次值{{值}}V，PT变比{{值}}kV/{{值}}V） |
    | 最大零序电流 | 零序电流最大值 | 同电流格式 |

    - **故障相电流**：用于故障基本情况表中的故障电流
    - **最大差动电流(Idiff)**：用于差流分析，必须明确标注为差流值
    - 注意区分：故障相电流 ≠ 差流，不得混用

11. 一次值换算：电流一次值(kA) = 二次值(A) × CT一次额定值 / CT二次额定值 / 1000；电压一次值(kV) = 二次值(V) × PT一次额定值(kV) / PT二次额定值(V)。无变比时只显示二次值并标注 [无CT变比] 或 [无PT变比]
12. 差流计算过程：输出时需展示换算过程，格式为 `{{二次值}}A × {{CT一次额定值}}/{{CT二次额定值}} = {{一次值}}kA`
    **计算示例（必须严格遵循）**：若二次值=6.68A，CT一次=3000A，CT二次=1A，则：6.68 × 3000 / 1 / 1000 = 20.04 kA。**结果必须是实际计算后的数值，不得将二次值原样作为一次值输出。**

## 编码处理
HDR 可能为 GB18030 或 UTF-8 编码。若读取乱码，尝试另一种编码重新解析。

## 错误处理
- 文件缺失：标注`[本套文件缺失]`
- 字段无数据：表格内用 `-` 表示，非表格位置写"未获取到数据"
- 有推测规则且可推测：标注`[推测为XXX]`
- **严禁使用模板占位符**：输出中不得出现 `{{值}}`、`{{结果}}`、`{{厂家名称}}`、`{{程序版本}}` 等未替换的占位符

## 数据来源标注
在段落末尾标注数据来源文件。
"""
