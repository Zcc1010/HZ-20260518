# -*- coding: utf-8 -*-
"""主变保护 Subagent Prompt"""


def build_subagent_prompt(
    hdr_content: str,
    rms_content: str,
    events_content: str,
    station: str,
    set_number: str,
) -> str:
    return f"""# 主变保护 Subagent Prompt 模板

## 任务

给定本套主变保护装置的录波文件（hdr + rms.csv + events.csv），按 HDR/Events/RMS 三个区块提取本套装置的关键信息，生成标准化的 Markdown 段落。

## 核心原则（必须遵守）

1. **严禁编造数据**：所有数据必须来自输入文件，不得凭空推测或编造。无数据时标注 `[无数据]`。
2. **数据溯源**：关键数据必须标注来源文件和字段名。
3. **保留原始值**：提取的数据必须与源文件一致，不得修改或"修正"原始数值。

## 设备信息

- 厂站: {station}
- 套别: {set_number}

## 输入数据

### HDR 文件内容
{hdr_content if hdr_content else '[HDR文件缺失]'}

### RMS CSV 文件内容
{rms_content if rms_content else '[RMS文件缺失]'}

### Events CSV 文件内容
{events_content if events_content else '[Events文件缺失]'}

## 输出格式

**重要：直接输出 Markdown 内容，不要用 ```markdown``` 代码围栏包裹。**

严格按照以下 Markdown 格式输出。有数据的字段填写实际值，无数据的标注`[无数据]`，有推测规则的标注`[推测为XXX]`。

**精简原则**：
- 当整个区块无数据时（如 HDR 文件缺失），只写一行 `[HDR文件缺失]`，**不要**逐字段列出 [无数据]
- Events 表只列出"动作"事件，省略"返回"事件
- RMS 信息只列出有突变值的通道
- **数字状态（DigitalStatus）只列出值为1的条目**，值为0的条目全部省略（包括"备用: 0"、未动作的保护功能等），不要逐条列出

## {station}_{set_number}

**装置型号**: {{型号}}，{{程序版本}}，{{智能站/常规站}}
**制造厂家**: {{厂家名称}}
**故障时间**: {{年}}-{{月}}-{{日}} {{时}}:{{分}}:{{秒}}.{{毫秒}}

### HDR信息
**设备信息**:
- 一次设备名称: {{值}}
- 装置型号: {{值}}
- 程序版本: {{值}}

**TripInfo（保护动作时间序列）**:
| 绝对时间 | 动作名称 | 相别 | 值 |
|----------|----------|------|-----|

**DigitalEvent（数字量事件）**:
| 绝对时间 | 名称 | 变化 |
|----------|------|------|

**FaultInfo（故障信息）**:
- 故障相电流: {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}）或 {{值}}A [无CT变比]
- 最大差动电流: {{一次值}}kA（二次值{{值}}A，CT变比{{值}}/{{值}}）或 {{值}}A [无CT变比]
- 故障相电压: {{一次值}}kV（二次值{{值}}V，PT变比{{值}}kV/{{值}}V）或 {{值}}V [无PT变比]

**定值信息**:
- 差动动作电流定值: {{值}}A
- 高压侧CT变比: {{CT一次额定值}}/{{CT二次额定值}} 或 [无数据]
- 中压侧CT变比: {{CT一次额定值}}/{{CT二次额定值}} 或 [无数据] [T5无中压侧]
- 低压侧CT变比: {{CT一次额定值}}/{{CT二次额定值}} 或 [无数据]
- 高压侧PT变比: {{PT一次额定值}}kV 或 [无数据]
- 高压侧额定容量: {{值}}MVA
- 停用重合闸: {{1=停用/0=未停用}}

### Events信息
| 绝对时间 | 通道名称 | 内容 |
|----------|----------|------|

### RMS信息
**各侧电流RMS最大值**（一次值）:
- 高压侧: IA: {{一次值}}kA（二次值{{值}}A）, IB: ..., IC: ..., 3I0: ...
- 中压侧: IA: ..., IB: ..., IC: ... [T5无中压侧]
- 低压侧: IA: ..., IB: ..., IC: ... [如有]

**各侧电压RMS最小值**（一次值）:
- 高压侧: UA: {{一次值}}kV（二次值{{值}}V）, UB: ..., UC: ...
- 中压侧: [T5无中压侧]
- 低压侧: [如有]

**差流**:
- 高压侧差流: {{一次值}}kA（二次值{{值}}A）或 {{值}}A [无CT变比]
- 中压侧差流: {{一次值}}kA（二次值{{值}}A） [T5无中压侧]
- 低压侧差流: {{一次值}}kA（二次值{{值}}A） [如有]

**电流电压突变**:
**第一次突变**（故障发生时）:
- {{侧别}}{{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- {{侧别}}{{通道名}}负突变: -{{值}}A @ HH:MM:SS.mmm
- ...（列出所有突变幅度超过阈值的通道，涵盖高/中/低压侧各通道）

**第二次突变**（重合后，如有）:
- {{侧别}}{{通道名}}正突变: +{{值}}A @ HH:MM:SS.mmm
- ...（无第二次突变时不输出此分组）

> 数据来源: {{hdr文件名}} + {{rms文件名}} + {{events文件名}}

## 关键提取规则

1. 厂站名和套别：{station}_{set_number}
- **严禁输出相对时间**（如 `0ms`、`15ms`），所有时间必须为绝对时间 `HH:MM:SS.mmm`
2. 装置型号：从 HDR `<DeviceInfo>` 提取，根据型号后缀判断智能站/常规站（含 FA/DA/DG → 智能站，其他 → 常规站）
3. 故障时间：从 HDR `<FaultStartTime>` 提取
4. TripInfo：从 HDR `<TripInfo>` 提取，偏移量转绝对时间
5. DigitalEvent：从 HDR `<DigitalEvent>` 提取
6. 定值信息：从 HDR `<SettingValue>` 提取关键定值（差动电流、各侧CT变比、PT变比、额定容量等）
7. Events信息：从 events.csv 提取，保持原始通道名
8. RMS信息：从 rms.csv 提取各侧电流/电压/差流/突变（按第一次/第二次分组，列出所有超过阈值的通道），全部换算为一次值
9. 变压器类型判断：T2（220kV，低压侧双分支）、T5（500kV，无中压侧）
10. CT变比和PT变比：从 HDR `<SettingValue>` 提取各侧 CT一次额定值/CT二次额定值/PT一次额定值（**不从 CFG 提取**）
11. FaultInfo：从 HDR `<FaultInfo>` 提取故障相电流、最大差动电流、故障相电压，全部换算为一次值
12. 一次值换算：电流一次值(kA) = 二次值(A) × CT一次额定值 / CT二次额定值 / 1000；无变比时标注 [无CT变比]

## 错误处理
- 文件缺失：标注`[本套文件缺失]`
- 字段无数据：标注`[无数据]`
- T5变压器无中压侧：标注"中压侧—（T5无中压侧）"
"""
